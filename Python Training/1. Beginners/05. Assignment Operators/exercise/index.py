# Challenge 1. Shorthand Updates
print("Challenge 1:")
count = 5
print(f"Count: {count}")
count += 3
print(f"Addition (count += 3): {count}")

count -= 2
print(f"Subtraction (count -= 2): {count}")

count *= 4
print(f"Multiplication (count *= 4): {count}")

count /= 2
print(f"Division (count /= 2): {count}")

